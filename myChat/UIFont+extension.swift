import Foundation
import UIKit

extension UIFont {
    static func avanir20() -> UIFont {
        return UIFont.init(name: "avanir", size: 20)!
    }
    static func avanir26() -> UIFont {
        return UIFont.init(name: "avanir", size: 26)!
    }
}

